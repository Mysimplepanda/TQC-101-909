def change(x):
    if x == 10:
        return  "A"
    elif x == 11:
        return "B"
    elif x == 12:
        return "C"
    elif x == 13:
        return "D"
    elif x == 14:
        return "E"
    elif x == 15:
        return "F"
    else:
        return x
print(change(int(input())))
    
